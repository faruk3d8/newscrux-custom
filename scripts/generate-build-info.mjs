#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const version = pkg.version ?? '0.0.0';
const builtAt = new Date().toISOString();

const out = `// Auto-generated by scripts/generate-build-info.mjs — do not edit
export const BUILD_INFO = {
  version: ${JSON.stringify(version)},
  builtAt: ${JSON.stringify(builtAt)},
} as const;

/** Telegram / log için sürüm numarası (semver, package.json). */
export function formatBuildInfoLine(): string {
  return \`v\${BUILD_INFO.version}\`;
}
`;

writeFileSync(join(root, 'src', 'build-info.ts'), out, 'utf8');
console.log(`build-info: v${version} @ ${builtAt}`);
